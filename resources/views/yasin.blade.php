<article class="main wrap mt-4">
	<h1 class="page-title">Surat Yasin</h1>
	<p class="bismillah">بِسْمِ اللّٰهِ الرَّحْمٰنِ الرَّحِيْمِ</p>
	<ol class="surah">
		<li>
			<p class="arabic" dir="rtl">يٰسۤ ۚ</p>
			<p class="translate">Yā sīn.</p>
			<p class="meaning">Yā Sīn.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَالْقُرْاٰنِ الْحَكِيْمِۙ</p>
			<p class="translate">Wal-qur'ānil-ḥakīm(i).</p>
			<p class="meaning">Demi Al-Qur’an yang penuh hikmah,</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِنَّكَ لَمِنَ الْمُرْسَلِيْنَۙ</p>
			<p class="translate">Innaka laminal-mursalīn(a).</p>
			<p class="meaning">sesungguhnya engkau (Nabi Muhammad) benar-benar salah seorang dari rasul-rasul</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">عَلٰى صِرَاطٍ مُّسْتَقِيْمٍۗ</p>
			<p class="translate">‘Alā ṣirāṭim mustaqīm(in).</p>
			<p class="meaning">(yang berada) di atas jalan yang lurus,</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">تَنْزِيْلَ الْعَزِيْزِ الرَّحِيْمِۙ</p>
			<p class="translate">Tanzīlal-‘azīzir-raḥīm(i).</p>
			<p class="meaning">(sebagai wahyu) yang diturunkan oleh (Allah) Yang Mahaperkasa lagi Maha Penyayang,</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">لِتُنْذِرَ قَوْمًا مَّآ اُنْذِرَ اٰبَاۤؤُهُمْ فَهُمْ غٰفِلُوْنَ</p>
			<p class="translate">Litunżira qaumam mā unżira ābā'uhum fahum gāfilūn(a).</p>
			<p class="meaning">agar engkau (Nabi Muhammad) memberi peringatan kepada suatu kaum yang nenek moyang mereka belum pernah diberi peringatan, sehingga mereka lalai.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">لَقَدْ حَقَّ الْقَوْلُ عَلٰٓى اَكْثَرِهِمْ فَهُمْ لَا يُؤْمِنُوْنَ</p>
			<p class="translate">Laqad ḥaqqal-qaulu ‘alā akṡarihim fahum lā yu'minūn(a).</p>
			<p class="meaning">Sungguh, benar-benar berlaku perkataan (ketetapan takdir) terhadap kebanyakan mereka, maka mereka tidak akan beriman.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِنَّا جَعَلْنَا فِيْٓ اَعْنَاقِهِمْ اَغْلٰلًا فَهِيَ اِلَى الْاَذْقَانِ فَهُمْ مُّقْمَحُوْنَ</p>
			<p class="translate">Innā ja‘alnā fī a‘nāqihim aglālan fa hiya ilal-ażqāni fahum muqmaḥūn(a).</p>
			<p class="meaning">Sesungguhnya Kami telah memasang belenggu di leher mereka, lalu (tangan mereka yang terbelenggu diangkat) ke dagu, karena itu mereka tertengadah.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَجَعَلْنَا مِنْۢ بَيْنِ اَيْدِيْهِمْ سَدًّا وَّمِنْ خَلْفِهِمْ سَدًّا فَاَغْشَيْنٰهُمْ فَهُمْ لَا يُبْصِرُوْنَ</p>
			<p class="translate">Wa ja‘alnā mim baini aidīhim saddaw wa min khalfihim saddan fa agsyaināhum fahum lā yubṣirūn(a).</p>
			<p class="meaning">Kami memasang penghalang di hadapan mereka dan di belakang mereka, sehingga Kami menutupi (pandangan) mereka. Mereka pun tidak dapat melihat.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَسَوَاۤءٌ عَلَيْهِمْ ءَاَنْذَرْتَهُمْ اَمْ لَمْ تُنْذِرْهُمْ لَا يُؤْمِنُوْنَ</p>
			<p class="translate">Wa sawā'un ‘alaihim a'anżartahum am lam tunżirhum lā yu'minūn(a).</p>
			<p class="meaning">Sama saja bagi mereka, apakah engkau (Nabi Muhammad) memberi peringatan kepada mereka atau tidak. Mereka (tetap) tidak akan beriman.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِنَّمَا تُنْذِرُ مَنِ اتَّبَعَ الذِّكْرَ وَخَشِيَ الرَّحْمٰنَ بِالْغَيْبِۚ فَبَشِّرْهُ بِمَغْفِرَةٍ وَّاَجْرٍ كَرِيْمٍ</p>
			<p class="translate">Innamā tunżiru manittaba‘aż-żikra wa khasyiyar-raḥmāna bil-gaib(i), fa basysyirhu bimagfiratiw wa ajrin karīm(in).</p>
			<p class="meaning">Sesungguhnya engkau (Nabi Muhammad) hanya (bisa) memberi peringatan kepada orang-orang yang mau mengikutinya dan yang takut kepada Tuhan Yang Maha Pengasih tanpa melihat-Nya. Berilah mereka kabar gembira dengan ampunan dan pahala yang mulia.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِنَّا نَحْنُ نُحْيِ الْمَوْتٰى وَنَكْتُبُ مَا قَدَّمُوْا وَاٰثَارَهُمْۗ وَكُلَّ شَيْءٍ اَحْصَيْنٰهُ فِيْٓ اِمَامٍ مُّبِيْنٍ ࣖ</p>
			<p class="translate">Innā naḥnu nuḥyil-mautā wa naktubu mā qaddamū wa āṡārahum, wa kulla syai'in aḥṣaināhu fī imāmim mubīn(in).</p>
			<p class="meaning">Sesungguhnya Kamilah yang menghidupkan orang-orang yang mati dan Kami (pulalah) yang mencatat apa yang telah mereka kerjakan dan bekas-bekas yang mereka (tinggalkan). Segala sesuatu Kami kumpulkan dalam kitab induk yang nyata (Lauh Mahfuz).</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَاضْرِبْ لَهُمْ مَّثَلًا اَصْحٰبَ الْقَرْيَةِۘ اِذْ جَاۤءَهَا الْمُرْسَلُوْنَۚ</p>
			<p class="translate">Waḍrib lahum maṡalan aṣḥābal-qaryah(ti), iż jā'ahal-mursalūn(a).</p>
			<p class="meaning">Buatlah suatu perumpamaan bagi mereka (kaum kafir Makkah), yaitu penduduk suatu negeri, ketika para utusan datang kepada mereka,</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِذْ اَرْسَلْنَآ اِلَيْهِمُ اثْنَيْنِ فَكَذَّبُوْهُمَا فَعَزَّزْنَا بِثَالِثٍ فَقَالُوْٓا اِنَّآ اِلَيْكُمْ مُّرْسَلُوْنَ</p>
			<p class="translate">Iż arsalnā ilaihimuṡnaini fa każżabūhumā fa ‘azzaznā biṡāliṡin faqālū innā ilaikum mursalūn(a).</p>
			<p class="meaning">(yaitu) ketika Kami mengutus kepada mereka dua orang utusan, lalu mereka mendustakan keduanya. Kemudian Kami menguatkan dengan (utusan) yang ketiga. Maka, ketiga (utusan itu) berkata, “Sesungguhnya kami adalah orang-orang yang diutus kepadamu.”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">قَالُوْا مَآ اَنْتُمْ اِلَّا بَشَرٌ مِّثْلُنَاۙ وَمَآ اَنْزَلَ الرَّحْمٰنُ مِنْ شَيْءٍۙ اِنْ اَنْتُمْ اِلَّا تَكْذِبُوْنَ</p>
			<p class="translate">Qālū mā antum illā basyarum miṡlunā, wa mā anzalar-raḥmānu min syai'(in), in antum illā takżibūn(a).</p>
			<p class="meaning">Mereka (penduduk negeri) menjawab, “Kamu tidak lain hanyalah manusia seperti kami. (Allah) Yang Maha Pengasih tidak (pernah) menurunkan sesuatu apa pun. Kamu hanyalah berdusta.”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">قَالُوْا رَبُّنَا يَعْلَمُ اِنَّآ اِلَيْكُمْ لَمُرْسَلُوْنَ</p>
			<p class="translate">Qālū rabbunā ya‘lamu innā ilaikum lamursalūn(a).</p>
			<p class="meaning">Mereka (para rasul) berkata, “Tuhan kami mengetahui bahwa sesungguhnya kami benar-benar para utusan(-Nya) kepadamu.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَمَا عَلَيْنَآ اِلَّا الْبَلٰغُ الْمُبِيْنُ</p>
			<p class="translate">Wa mā ‘alainā illal-balāgul-mubīn(u).</p>
			<p class="meaning">Adapun kewajiban kami hanyalah menyampaikan (perintah Allah) yang jelas.”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">قَالُوْٓا اِنَّا تَطَيَّرْنَا بِكُمْۚ لَىِٕنْ لَّمْ تَنْتَهُوْا لَنَرْجُمَنَّكُمْ وَلَيَمَسَّنَّكُمْ مِّنَّا عَذَابٌ اَلِيْمٌ</p>
			<p class="translate">Qālū innā taṭayyarnā bikum, la'il lam tantahū lanarjumannakum wa layamassannakum minnā ‘ażābun alīm(un).</p>
			<p class="meaning">Mereka (penduduk negeri) menjawab, “Sesungguhnya kami bernasib malang karenamu. Sungguh, jika kamu tidak berhenti (menyeru kami), niscaya kami merajam kamu dan kamu pasti akan merasakan siksaan yang pedih dari kami.”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">قَالُوْا طَاۤىِٕرُكُمْ مَّعَكُمْۗ اَىِٕنْ ذُكِّرْتُمْۗ بَلْ اَنْتُمْ قَوْمٌ مُّسْرِفُوْنَ</p>
			<p class="translate">Qālū ṭā'irukum ma‘akum, a'in żukkirtum, bal antum qaumum musrifūn(a).</p>
			<p class="meaning">Mereka (para rasul) berkata, “Kemalangan kamu itu (akibat perbuatan) kamu sendiri. Apakah karena kamu diberi peringatan, (lalu kamu menjadi malang)? Sebenarnya kamu adalah kaum yang melampaui batas.”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَجَاۤءَ مِنْ اَقْصَا الْمَدِيْنَةِ رَجُلٌ يَّسْعٰى قَالَ يٰقَوْمِ اتَّبِعُوا الْمُرْسَلِيْنَۙ</p>
			<p class="translate">Wa jā'a min aqṣal-madīnati rajuluy yas‘ā qāla yā qaumittabi‘ul-mursalīn(a).</p>
			<p class="meaning">Datanglah dengan bergegas dari ujung kota, seorang laki-laki. Dia berkata, “Wahai kaumku, ikutilah para rasul itu!</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اتَّبِعُوْا مَنْ لَّا يَسْـَٔلُكُمْ اَجْرًا وَّهُمْ مُّهْتَدُوْنَ ۔</p>
			<p class="translate">Ittabi‘ū mal lā yas'alukum ajraw wa hum muhtadūn(a).</p>
			<p class="meaning">Ikutilah orang yang tidak meminta imbalan (dalam berdakwah) kepadamu. Mereka adalah orang-orang yang mendapat petunjuk.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَمَا لِيَ لَآ اَعْبُدُ الَّذِيْ فَطَرَنِيْ وَاِلَيْهِ تُرْجَعُوْنَ</p>
			<p class="translate">Wa mā liya lā a‘budul-lażī faṭaranī wa ilaihi turja‘ūn(a).</p>
			<p class="meaning">Apa (alasanku) untuk tidak menyembah (Allah) yang telah menciptakanku dan hanya kepada-Nyalah kamu akan dikembalikan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">ءَاَتَّخِذُ مِنْ دُوْنِهٖٓ اٰلِهَةً اِنْ يُّرِدْنِ الرَّحْمٰنُ بِضُرٍّ لَّا تُغْنِ عَنِّيْ شَفَاعَتُهُمْ شَيْـًٔا وَّلَا يُنْقِذُوْنِۚ</p>
			<p class="translate">A'attakhiżu min dūnihī ālihatan iy yuridnir-raḥmānu biḍurril lā tugni ‘annī syafā‘atuhum syai'aw wa lā yunqiżūn(i).</p>
			<p class="meaning">Mengapa aku (harus) mengambil sembahan-sembahan selain-Nya? Jika (Allah) Yang Maha Pengasih menghendaki bencana terhadapku, pasti pertolongan mereka tidak berguna sama sekali bagi diriku dan mereka (juga) tidak dapat menyelamatkanku.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِنِّيْٓ اِذًا لَّفِيْ ضَلٰلٍ مُّبِيْنٍ</p>
			<p class="translate">Innī iżal lafī ḍalālim mubīn(in).</p>
			<p class="meaning">Sesungguhnya aku (jika berbuat) begitu, pasti berada dalam kesesatan yang nyata.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِنِّيْٓ اٰمَنْتُ بِرَبِّكُمْ فَاسْمَعُوْنِۗ</p>
			<p class="translate">Innī āmantu birabbikum fasma‘ūn(i).</p>
			<p class="meaning">Sesungguhnya aku telah beriman kepada Tuhanmu. Maka, dengarkanlah (pengakuan)-ku.”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">قِيْلَ ادْخُلِ الْجَنَّةَ ۗقَالَ يٰلَيْتَ قَوْمِيْ يَعْلَمُوْنَۙ</p>
			<p class="translate">Qīladkhulil-jannah(ta), qāla yā laita qaumī ya‘lamūn(a).</p>
			<p class="meaning">Dikatakan (kepadanya), “Masuklah ke surga.” Dia (laki-laki itu) berkata, “Aduhai, sekiranya kaumku mengetahui</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">بِمَا غَفَرَ لِيْ رَبِّيْ وَجَعَلَنِيْ مِنَ الْمُكْرَمِيْنَ</p>
			<p class="translate">Bimā gafaralī rabbī wa ja‘alanī minal-mukramīn(a).</p>
			<p class="meaning">(bagaimana) Tuhanku mengampuniku dan menjadikanku termasuk orang-orang yang dimuliakan.”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">۞ وَمَآ اَنْزَلْنَا عَلٰى قَوْمِهٖ مِنْۢ بَعْدِهٖ مِنْ جُنْدٍ مِّنَ السَّمَاۤءِ وَمَا كُنَّا مُنْزِلِيْنَ</p>
			<p class="translate">Wa mā anzalnā ‘alā qaumihī mim ba‘dihī min jundim minas-samā'i wa mā kunnā munzilīn(a).</p>
			<p class="meaning">Setelah dia (dibunuh), Kami tidak menurunkan satu pasukan pun dari langit kepada kaumnya dan Kami tidak perlu menurunkannya.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِنْ كَانَتْ اِلَّا صَيْحَةً وَّاحِدَةً فَاِذَا هُمْ خٰمِدُوْنَ</p>
			<p class="translate">In kānat illā ṣaiḥataw wāḥidatan fa'iżā hum khāmidūn(a).</p>
			<p class="meaning">(Azab mereka) itu cukup dengan satu teriakan saja. Maka, seketika itu mereka mati.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">يٰحَسْرَةً عَلَى الْعِبَادِۚ مَا يَأْتِيْهِمْ مِّنْ رَّسُوْلٍ اِلَّا كَانُوْا بِهٖ يَسْتَهْزِءُوْنَ</p>
			<p class="translate">Yā ḥasratan ‘alal-‘ibād(i), mā ya'tīhim mir rasūlin illā kānū bihī yastahzi'ūn(a).</p>
			<p class="meaning">Alangkah besar penyesalan diri para hamba itu. Setiap datang seorang rasul kepada mereka, mereka selalu memperolok-olokkannya.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اَلَمْ يَرَوْا كَمْ اَهْلَكْنَا قَبْلَهُمْ مِّنَ الْقُرُوْنِ اَنَّهُمْ اِلَيْهِمْ لَا يَرْجِعُوْنَ</p>
			<p class="translate">Alam yarau kam ahlaknā qablahum minal-qurūni annahum ilaihim lā yarji‘ūn(a).</p>
			<p class="meaning">Tidakkah mereka mengetahui berapa banyak umat sebelum mereka yang telah Kami binasakan. Mereka (setelah binasa) tidak ada yang kembali kepada mereka (di dunia).</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَاِنْ كُلٌّ لَّمَّا جَمِيْعٌ لَّدَيْنَا مُحْضَرُوْنَ ࣖ</p>
			<p class="translate">Wa in kullul lammā jamī‘ul ladainā muḥḍarūn(a).</p>
			<p class="meaning">Tidak ada satu (umat) pun, kecuali semuanya akan dihadirkan kepada Kami (untuk dihisab).</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَاٰيَةٌ لَّهُمُ الْاَرْضُ الْمَيْتَةُ ۖاَحْيَيْنٰهَا وَاَخْرَجْنَا مِنْهَا حَبًّا فَمِنْهُ يَأْكُلُوْنَ</p>
			<p class="translate">Wa āyatul lahumul-arḍul-maitah(tu), aḥyaināhā wa akhrajnā minhā ḥabban faminhu ya'kulūn(a).</p>
			<p class="meaning">Suatu tanda (kekuasaan-Nya) bagi mereka adalah bumi yang mati (tandus lalu) Kami menghidupkannya dan mengeluarkan darinya biji-bijian kemudian dari (biji-bijian) itu mereka makan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَجَعَلْنَا فِيْهَا جَنّٰتٍ مِّنْ نَّخِيْلٍ وَّاَعْنَابٍ وَّفَجَّرْنَا فِيْهَا مِنَ الْعُيُوْنِۙ</p>
			<p class="translate">Wa ja‘alnā fīhā jannātim min nakhīliw wa a‘nābiw wa fajjarnā fīhā minal-‘uyūn(i).</p>
			<p class="meaning">Kami (juga) menjadikan padanya (bumi) kebun-kebun kurma dan anggur serta Kami memancarkan padanya beberapa mata air</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">لِيَأْكُلُوْا مِنْ ثَمَرِهٖۙ وَمَا عَمِلَتْهُ اَيْدِيْهِمْ ۗ اَفَلَا يَشْكُرُوْنَ</p>
			<p class="translate">Liya'kulū min ṡamarihī wa mā ‘amilathu aidīhim, afalā yasykurūn(a).</p>
			<p class="meaning">agar mereka dapat makan dari buahnya, dan dari hasil usaha tangan mereka. Mengapa mereka tidak bersyukur?</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">سُبْحٰنَ الَّذِيْ خَلَقَ الْاَزْوَاجَ كُلَّهَا مِمَّا تُنْۢبِتُ الْاَرْضُ وَمِنْ اَنْفُسِهِمْ وَمِمَّا لَا يَعْلَمُوْنَ</p>
			<p class="translate">Subḥānal-lażī khalaqal-azwāja kullahā mimmā tumbitul-arḍu wa min anfusihim wa mimmā lā ya‘lamūn(a).</p>
			<p class="meaning">Mahasuci (Allah) yang telah menciptakan semuanya berpasang-pasangan, baik dari apa yang ditumbuhkan oleh bumi dan dari diri mereka sendiri maupun dari apa yang tidak mereka ketahui.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَاٰيَةٌ لَّهُمُ الَّيْلُ ۖنَسْلَخُ مِنْهُ النَّهَارَ فَاِذَا هُمْ مُّظْلِمُوْنَۙ</p>
			<p class="translate">Wa āyatul lahumul-lailu naslakhu minhun-nahāra fa'iżā hum muẓlimūn(a).</p>
			<p class="meaning">Suatu tanda juga (atas kekuasaan Allah) bagi mereka adalah malam. Kami pisahkan siang dari (malam) itu. Maka, seketika itu mereka (berada dalam) kegelapan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَالشَّمْسُ تَجْرِيْ لِمُسْتَقَرٍّ لَّهَا ۗذٰلِكَ تَقْدِيْرُ الْعَزِيْزِ الْعَلِيْمِۗ</p>
			<p class="translate">Wasy-syamsu tajrī limustaqarril lahā, żālika taqdīrul-‘azīzil-‘alīm(i).</p>
			<p class="meaning">(Suatu tanda juga atas kekuasaan Allah bagi mereka adalah) matahari yang berjalan di tempat peredarannya. Demikianlah ketetapan (Allah) Yang Mahaperkasa lagi Maha Mengetahui.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَالْقَمَرَ قَدَّرْنٰهُ مَنَازِلَ حَتّٰى عَادَ كَالْعُرْجُوْنِ الْقَدِيْمِ</p>
			<p class="translate">Wal-qamara qaddarnāhu manāzila ḥattā ‘āda kal-‘urjūnil-qadīm(i).</p>
			<p class="meaning">(Begitu juga) bulan, Kami tetapkan bagi(-nya) tempat-tempat peredaran sehingga (setelah ia sampai ke tempat peredaran yang terakhir,) kembalilah ia seperti bentuk tandan yang tua.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">لَا الشَّمْسُ يَنْۢبَغِيْ لَهَآ اَنْ تُدْرِكَ الْقَمَرَ وَلَا الَّيْلُ سَابِقُ النَّهَارِ ۗوَكُلٌّ فِيْ فَلَكٍ يَّسْبَحُوْنَ</p>
			<p class="translate">Lasy-syamsu yambagī lahā an tudrikal-qamara wa lal-lailu sābiqun-nahār(i), wa kullun fī falakiy yasbaḥūn(a).</p>
			<p class="meaning">Tidaklah mungkin bagi matahari mengejar bulan dan malam pun tidak dapat mendahului siang. Masing-masing beredar pada garis edarnya.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَاٰيَةٌ لَّهُمْ اَنَّا حَمَلْنَا ذُرِّيَّتَهُمْ فِى الْفُلْكِ الْمَشْحُوْنِۙ</p>
			<p class="translate">Wa āyatul lahum annā ḥamalnā żurriyyatahum fil-fulkil-masyḥūn(i).</p>
			<p class="meaning">Suatu tanda (kebesaran Allah) bagi mereka adalah bahwa Kami mengangkut keturunan mereka dalam kapal yang penuh muatan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَخَلَقْنَا لَهُمْ مِّنْ مِّثْلِهٖ مَا يَرْكَبُوْنَ</p>
			<p class="translate">Wa khalaqnā lahum mim miṡlihī mā yarkabūn(a).</p>
			<p class="meaning">(Begitu juga) Kami menciptakan untuk mereka dari jenis itu angkutan (lain) yang mereka kendarai.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَاِنْ نَّشَأْ نُغْرِقْهُمْ فَلَا صَرِيْخَ لَهُمْ وَلَاهُمْ يُنْقَذُوْنَۙ</p>
			<p class="translate">Wa in nasya' nugriqhum falā ṣarīkha lahum wa lā hum yunqażūn(a).</p>
			<p class="meaning">Jika Kami menghendaki, Kami akan menenggelamkan mereka. Kemudian, tidak ada penolong bagi mereka dan tidak (pula) mereka diselamatkan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِلَّا رَحْمَةً مِّنَّا وَمَتَاعًا اِلٰى حِيْنٍ</p>
			<p class="translate">Illā raḥmatam minnā wa matā‘an ilā ḥīn(in).</p>
			<p class="meaning">Akan tetapi, (Kami menyelamatkan mereka) karena rahmat yang besar dari Kami dan untuk memberi mereka kesenangan hidup sampai waktu tertentu.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَاِذَا قِيْلَ لَهُمُ اتَّقُوْا مَا بَيْنَ اَيْدِيْكُمْ وَمَا خَلْفَكُمْ لَعَلَّكُمْ تُرْحَمُوْنَ</p>
			<p class="translate">Wa iżā qīla lahumuttaqū mā baina aidīkum wa mā khalfakum la‘allakum turḥamūn(a).</p>
			<p class="meaning">Ketika dikatakan kepada mereka, “Takutlah kamu akan (siksa) yang ada di hadapanmu (di dunia) dan azab yang ada di belakangmu (akhirat) agar kamu mendapat rahmat,” (maka mereka berpaling).</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَمَا تَأْتِيْهِمْ مِّنْ اٰيَةٍ مِّنْ اٰيٰتِ رَبِّهِمْ اِلَّا كَانُوْا عَنْهَا مُعْرِضِيْنَ</p>
			<p class="translate">Wa mā ta'tīhim min āyatim min āyāti rabbihim illā kānū ‘anhā mu‘riḍīn(a).</p>
			<p class="meaning">Tidak satu pun dari tanda-tanda (kebesaran) Tuhan datang kepada mereka, kecuali mereka berpaling darinya.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَاِذَا قِيْلَ لَهُمْ اَنْفِقُوْا مِمَّا رَزَقَكُمُ اللّٰهُ ۙقَالَ الَّذِيْنَ كَفَرُوْا لِلَّذِيْنَ اٰمَنُوْٓا اَنُطْعِمُ مَنْ لَّوْ يَشَاۤءُ اللّٰهُ اَطْعَمَهٗٓ ۖاِنْ اَنْتُمْ اِلَّا فِيْ ضَلٰلٍ مُّبِيْنٍ</p>
			<p class="translate">Wa iżā qīla lahum anfiqū mimmā razaqakumullāh(u), qālal-lażīna kafarū lil-lażīna āmanū anuṭ‘imu mal lau yasyā'ullāhu aṭ‘amah(ū), in antum illā fī ḍalālim mubīn(in).</p>
			<p class="meaning">Apabila dikatakan kepada mereka, “Infakkanlah sebagian rezeki yang diberikan Allah kepadamu,” orang-orang yang kufur itu berkata kepada orang-orang yang beriman, “Apakah pantas kami memberi makan kepada orang-orang yang jika Allah menghendaki, Dia akan memberinya makan? Kamu benar-benar dalam kesesatan yang nyata.”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَيَقُوْلُوْنَ مَتٰى هٰذَا الْوَعْدُ اِنْ كُنْتُمْ صٰدِقِيْنَ</p>
			<p class="translate">Wa yaqūlūna matā hāżal-wa‘du in kuntum ṣādiqīn(a).</p>
			<p class="meaning">Mereka berkata, “Kapankah janji (hari Kebangkitan) ini (terjadi) jika kamu orang-orang benar?”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">مَا يَنْظُرُوْنَ اِلَّا صَيْحَةً وَّاحِدَةً تَأْخُذُهُمْ وَهُمْ يَخِصِّمُوْنَ</p>
			<p class="translate">Mā yanẓurūna illā ṣaiḥataw wāḥidatan ta'khużuhum wa hum yakhiṣṣimūn(a).</p>
			<p class="meaning">Mereka hanya menunggu satu teriakan yang akan membinasakan mereka saat mereka (sibuk) bertengkar (tentang urusan dunia).</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">فَلَا يَسْتَطِيْعُوْنَ تَوْصِيَةً وَّلَآ اِلٰٓى اَهْلِهِمْ يَرْجِعُوْنَ ࣖ</p>
			<p class="translate">Falā yastaṭī‘ūna tauṣiyataw wa lā ilā ahlihim yarji‘ūn(a).</p>
			<p class="meaning">Oleh sebab itu, mereka tidak dapat berwasiat dan tidak dapat kembali kepada keluarganya.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَنُفِخَ فِى الصُّوْرِ فَاِذَا هُمْ مِّنَ الْاَجْدَاثِ اِلٰى رَبِّهِمْ يَنْسِلُوْنَ</p>
			<p class="translate">Wa nufikha fiṣ-ṣūri fa'iżā hum minal-ajdāṡi ilā rabbihim yansilūn(a).</p>
			<p class="meaning">Sangkakala pun ditiup dan seketika itu mereka bergerak cepat dari kuburnya menuju kepada Tuhannya.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">قَالُوْا يٰوَيْلَنَا مَنْۢ بَعَثَنَا مِنْ مَّرْقَدِنَا ۜهٰذَا مَا وَعَدَ الرَّحْمٰنُ وَصَدَقَ الْمُرْسَلُوْنَ</p>
			<p class="translate">Qālū yā wailanā mam ba‘aṡanā mim marqadinā…hāżā mā wa‘adar-raḥmānu wa ṣadaqal-mursalūn(a).</p>
			<p class="meaning">Mereka berkata, “Celakalah kami! Siapakah yang membangkitkan kami dari tempat tidur kami (kubur)?” (Lalu, dikatakan kepada mereka,) “Inilah yang dijanjikan (Allah) Yang Maha Pengasih dan benarlah para rasul(-Nya).”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِنْ كَانَتْ اِلَّا صَيْحَةً وَّاحِدَةً فَاِذَا هُمْ جَمِيْعٌ لَّدَيْنَا مُحْضَرُوْنَ</p>
			<p class="translate">In kānat illā ṣaiḥataw wāḥidatan fa'iżā hum jamī‘ul ladainā muḥḍarūn(a).</p>
			<p class="meaning">Teriakan itu hanya sekali saja, maka seketika itu mereka semua dihadapkan kepada Kami (untuk dihisab).</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">فَالْيَوْمَ لَا تُظْلَمُ نَفْسٌ شَيْـًٔا وَّلَا تُجْزَوْنَ اِلَّا مَا كُنْتُمْ تَعْمَلُوْنَ</p>
			<p class="translate">Fal-yauma lā tuẓlamu nafsun syai'aw wa lā tujzauna illā mā kuntum ta‘malūn(a).</p>
			<p class="meaning">Pada hari itu tidak ada sama sekali orang yang dirugikan sedikit pun. Kamu tidak akan diberi balasan, kecuali atas apa yang telah kamu kerjakan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِنَّ اَصْحٰبَ الْجَنَّةِ الْيَوْمَ فِيْ شُغُلٍ فٰكِهُوْنَ ۚ</p>
			<p class="translate">Inna aṣḥābal-jannatil-yauma fī syugulin fākihūn(a).</p>
			<p class="meaning">Sesungguhnya penghuni surga pada hari itu berada dalam kesibukan (sehingga tidak sempat berpikir tentang penghuni neraka) lagi bersenang-senang.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">هُمْ وَاَزْوَاجُهُمْ فِيْ ظِلٰلٍ عَلَى الْاَرَاۤىِٕكِ مُتَّكِـُٔوْنَ ۚ</p>
			<p class="translate">Hum wa azwājuhum fī ẓilālin ‘alal-arā'iki muttaki'ūn(a).</p>
			<p class="meaning">Mereka dan pasangan-pasangannya berada dalam tempat yang teduh sambil berbaring di atas ranjang berkelambu.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">لَهُمْ فِيْهَا فَاكِهَةٌ وَّلَهُمْ مَّا يَدَّعُوْنَ ۚ</p>
			<p class="translate">Lahum fīhā fākihatuw wa lahum mā yadda‘ūn(a).</p>
			<p class="meaning">Di (surga) itu mereka memperoleh buah-buahan dan apa saja yang mereka inginkan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">سَلٰمٌۗ قَوْلًا مِّنْ رَّبٍّ رَّحِيْمٍ</p>
			<p class="translate">Salāmun qaulam mir rabbir raḥīm(in).</p>
			<p class="meaning">(Kepada mereka dikatakan,) “Salam sejahtera” sebagai ucapan dari Tuhan Yang Maha Penyayang.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَامْتَازُوا الْيَوْمَ اَيُّهَا الْمُجْرِمُوْنَ</p>
			<p class="translate">Wamtāzul-yauma ayyuhal-mujrimūn(a).</p>
			<p class="meaning">(Dikatakan kepada orang-orang kafir,) “Berpisahlah kamu (dari orang-orang mukmin) pada hari ini, wahai para pendurhaka!</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">۞ اَلَمْ اَعْهَدْ اِلَيْكُمْ يٰبَنِيْٓ اٰدَمَ اَنْ لَّا تَعْبُدُوا الشَّيْطٰنَۚ اِنَّهٗ لَكُمْ عَدُوٌّ مُّبِيْنٌ</p>
			<p class="translate">Alam a‘had ilaikum yā banī ādama allā ta‘budusy-syaiṭān(a), innahū lakum ‘aduwwum mubīn(un).</p>
			<p class="meaning">Bukankah Aku telah berpesan kepadamu dengan sungguh-sungguh, wahai anak cucu Adam, bahwa janganlah kamu menyembah setan? Sesungguhnya setan itu musuh yang nyata bagi kamu.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَاَنِ اعْبُدُوْنِيْ ۗهٰذَا صِرَاطٌ مُّسْتَقِيْمٌ</p>
			<p class="translate">Wa ani‘budūnī, hāżā ṣirāṭum mustaqīm(un).</p>
			<p class="meaning">(Begitu juga bahwa) sembahlah Aku. Inilah jalan yang lurus.”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَلَقَدْ اَضَلَّ مِنْكُمْ جِبِلًّا كَثِيْرًا ۗاَفَلَمْ تَكُوْنُوْا تَعْقِلُوْنَ</p>
			<p class="translate">Wa laqad aḍalla minkum jibillan kaṡīrā(n), afalam takūnū ta‘qilūn(a).</p>
			<p class="meaning">Sungguh, ia (setan itu) benar-benar telah menyesatkan sangat banyak orang dari kamu. Maka, apakah kamu tidak mengerti?</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">هٰذِهٖ جَهَنَّمُ الَّتِيْ كُنْتُمْ تُوْعَدُوْنَ</p>
			<p class="translate">Hāżihī jahannamul-latī kuntum tū‘adūn(a).</p>
			<p class="meaning">Inilah (neraka) Jahanam yang dahulu telah diperingatkan kepadamu.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِصْلَوْهَا الْيَوْمَ بِمَا كُنْتُمْ تَكْفُرُوْنَ</p>
			<p class="translate">Iṣlauhal-yauma bimā kuntum takfurūn(a).</p>
			<p class="meaning">Masuklah ke dalamnya pada hari ini karena dahulu kamu mengingkarinya.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اَلْيَوْمَ نَخْتِمُ عَلٰٓى اَفْوَاهِهِمْ وَتُكَلِّمُنَآ اَيْدِيْهِمْ وَتَشْهَدُ اَرْجُلُهُمْ بِمَا كَانُوْا يَكْسِبُوْنَ</p>
			<p class="translate">Al-yauma nakhtimu ‘alā afwāhihim wa tukallimunā aidīhim wa tasyhadu arjuluhum bimā kānū yaksibūn(a).</p>
			<p class="meaning">Pada hari ini Kami membungkam mulut mereka. Tangan merekalah yang berkata kepada Kami dan kaki merekalah yang akan bersaksi terhadap apa yang dahulu mereka kerjakan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَلَوْ نَشَاۤءُ لَطَمَسْنَا عَلٰٓى اَعْيُنِهِمْ فَاسْتَبَقُوا الصِّرَاطَ فَاَنّٰى يُبْصِرُوْنَ</p>
			<p class="translate">Wa lau nasyā'u laṭamasnā ‘alā a‘yunihim fastabaquṣ-ṣirāṭa fa annā yubṣirūn(a).</p>
			<p class="meaning">Seandainya Kami menghendaki, pastilah Kami akan menghapus penglihatan (membutakan) mereka sehingga mereka berlomba-lomba (mencari) jalan (selamat). Maka, bagaimana mungkin mereka dapat melihat?</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَلَوْ نَشَاۤءُ لَمَسَخْنٰهُمْ عَلٰى مَكَانَتِهِمْ فَمَا اسْتَطَاعُوْا مُضِيًّا وَّلَا يَرْجِعُوْنَ ࣖ</p>
			<p class="translate">Wa lau nasyā'u lamasakhnāhum ‘alā makānatihim famastaṭā‘ū muḍiyyaw wa lā yarji‘ūn(a).</p>
			<p class="meaning">Seandainya Kami menghendaki, pastilah Kami akan mengubah bentuk mereka di tempat mereka berada, sehingga mereka tidak sanggup meneruskan perjalanan dan juga tidak sanggup pulang kembali.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَمَنْ نُّعَمِّرْهُ نُنَكِّسْهُ فِى الْخَلْقِۗ اَفَلَا يَعْقِلُوْنَ</p>
			<p class="translate">Wa man nu‘ammirhu nunakkishu fil-khalq(i), afalā ya‘qilūn(a).</p>
			<p class="meaning">Siapa yang Kami panjangkan umurnya niscaya Kami balik proses penciptaannya (dari kuat menuju lemah). Maka, apakah mereka tidak mengerti?</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَمَا عَلَّمْنٰهُ الشِّعْرَ وَمَا يَنْۢبَغِيْ لَهٗ ۗاِنْ هُوَ اِلَّا ذِكْرٌ وَّقُرْاٰنٌ مُّبِيْنٌ ۙ</p>
			<p class="translate">Wa mā ‘allamnāhusy-syi‘ra wa mā yambagī lah(ū), in huwa illā żikruw wa qur'ānum mubīn(un).</p>
			<p class="meaning">Kami tidak mengajarkan syair kepadanya (Nabi Muhammad) dan (bersyair) itu tidaklah pantas baginya. (Wahyu yang Kami turunkan kepadanya) itu tidak lain hanyalah pelajaran dan Al-Qur’an yang jelas,</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">لِّيُنْذِرَ مَنْ كَانَ حَيًّا وَّيَحِقَّ الْقَوْلُ عَلَى الْكٰفِرِيْنَ</p>
			<p class="translate">Liyunżira man kāna ḥayyaw wa yaḥiqqal-qaulu ‘alal-kāfirīn(a).</p>
			<p class="meaning">agar dia (Nabi Muhammad) memberi peringatan kepada orang-orang yang hidup (hatinya) dan agar ketetapan (azab) terhadap orang-orang kafir itu menjadi pasti.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اَوَلَمْ يَرَوْا اَنَّا خَلَقْنَا لَهُمْ مِّمَّا عَمِلَتْ اَيْدِيْنَآ اَنْعَامًا فَهُمْ لَهَا مٰلِكُوْنَ</p>
			<p class="translate">Awalam yarau annā khalaqnā lahum mimmā ‘amilat aidīnā an‘āman fahum lahā mālikūn(a).</p>
			<p class="meaning">Tidakkah mereka mengetahui bahwa Kami telah menciptakan untuk mereka hewan-hewan ternak dari ciptaan tangan Kami (sendiri), lalu mereka menjadi pemiliknya?</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَذَلَّلْنٰهَا لَهُمْ فَمِنْهَا رَكُوْبُهُمْ وَمِنْهَا يَأْكُلُوْنَ</p>
			<p class="translate">Wa żallalnāhā lahum fa minhā rakūbuhum wa minhā ya'kulūn(a).</p>
			<p class="meaning">Kami menjadikannya (hewan-hewan itu) tunduk kepada mereka. Sebagian di antaranya menjadi tunggangan mereka dan sebagian (lagi) mereka makan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَلَهُمْ فِيْهَا مَنَافِعُ وَمَشَارِبُۗ اَفَلَا يَشْكُرُوْنَ</p>
			<p class="translate">Wa lahum fīhā manāfi‘u wa masyārib(u), afalā yasykurūn(a).</p>
			<p class="meaning">Pada dirinya (hewan-hewan ternak itu) terdapat berbagai manfaat dan minuman untuk mereka. Apakah mereka tidak bersyukur?</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَاتَّخَذُوْا مِنْ دُوْنِ اللّٰهِ اٰلِهَةً لَّعَلَّهُمْ يُنْصَرُوْنَ ۗ</p>
			<p class="translate">Wattakhażū min dūnillāhi ālihatal la‘allahum yunṣarūn(a).</p>
			<p class="meaning">Mereka menjadikan sesembahan selain Allah agar mereka mendapat pertolongan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">لَا يَسْتَطِيْعُوْنَ نَصْرَهُمْۙ وَهُمْ لَهُمْ جُنْدٌ مُّحْضَرُوْنَ</p>
			<p class="translate">Lā yastaṭī‘ūna naṣrahum, wa hum lahum jundum muḥḍarūn(a).</p>
			<p class="meaning">(Sesembahan) itu tidak mampu menolong mereka, padahal (sesembahan) itu adalah tentara yang dihadirkan untuk menjaganya.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">فَلَا يَحْزُنْكَ قَوْلُهُمْ ۘاِنَّا نَعْلَمُ مَا يُسِرُّوْنَ وَمَا يُعْلِنُوْنَ</p>
			<p class="translate">Falā yaḥzunka qauluhum, innā na‘lamu mā yusirrūna wa mā yu‘linūn(a).</p>
			<p class="meaning">Maka, jangan sampai ucapan mereka membuat engkau (Nabi Muhammad) bersedih hati. Sesungguhnya Kami mengetahui apa yang mereka rahasiakan dan apa yang mereka nyatakan.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اَوَلَمْ يَرَ الْاِنْسَانُ اَنَّا خَلَقْنٰهُ مِنْ نُّطْفَةٍ فَاِذَا هُوَ خَصِيْمٌ مُّبِيْنٌ</p>
			<p class="translate">Awalam yaral-insānu annā khalaqnāhu min nuṭfatin fa'iżā huwa khaṣīmum mubīn(un).</p>
			<p class="meaning">Tidakkah manusia mengetahui bahwa Kami menciptakannya dari setetes mani? Kemudian tiba-tiba saja dia menjadi musuh yang nyata.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">وَضَرَبَ لَنَا مَثَلًا وَّنَسِيَ خَلْقَهٗۗ قَالَ مَنْ يُّحْيِ الْعِظَامَ وَهِيَ رَمِيْمٌ</p>
			<p class="translate">Wa ḍaraba lanā maṡalaw wa nasiya khalqah(ū), qāla may yuḥyil-‘iẓāma wa hiya ramīm(un).</p>
			<p class="meaning">Dia membuat perumpamaan bagi Kami dan melupakan asal penciptaannya. Dia berkata, “Siapakah yang bisa menghidupkan tulang-belulang yang telah hancur luluh?”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">قُلْ يُحْيِيْهَا الَّذِيْٓ اَنْشَاَهَآ اَوَّلَ مَرَّةٍ ۗوَهُوَ بِكُلِّ خَلْقٍ عَلِيْمٌ ۙ</p>
			<p class="translate">Qul yuḥyīhal-lażī ansya'ahā awwala marrah(tin), wa huwa bikulli khalqin ‘alīm(un).</p>
			<p class="meaning">Katakanlah (Nabi Muhammad), “Yang akan menghidupkannya adalah Zat yang menciptakannya pertama kali. Dia Maha Mengetahui setiap makhluk.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">ۨالَّذِيْ جَعَلَ لَكُمْ مِّنَ الشَّجَرِ الْاَخْضَرِ نَارًاۙ فَاِذَآ اَنْتُمْ مِّنْهُ تُوْقِدُوْنَ</p>
			<p class="translate">Allażī ja‘ala lakum minasy-syajaril-akhḍari nārā(n), fa'iżā antum minhu tūqidūn(a).</p>
			<p class="meaning">(Dialah) yang menjadikan api untukmu dari kayu yang hijau. Kemudian, seketika itu kamu menyalakan (api) darinya.”</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اَوَلَيْسَ الَّذِيْ خَلَقَ السَّمٰوٰتِ وَالْاَرْضَ بِقٰدِرٍ عَلٰٓى اَنْ يَّخْلُقَ مِثْلَهُمْ ۗبَلٰى وَهُوَ الْخَلّٰقُ الْعَلِيْمُ</p>
			<p class="translate">Awa laisal-lażī khalaqas-samāwāti wal-arḍa biqādirin ‘alā ay yakhluqa miṡlahum, balā wa huwal-khallāqul-‘alīm(u).</p>
			<p class="meaning">Bukankah Zat yang menciptakan langit dan bumi mampu menciptakan manusia yang serupa mereka itu (di akhirat kelak)? Benar. Dialah yang Maha Banyak Mencipta lagi Maha Mengetahui.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">اِنَّمَآ اَمْرُهٗٓ اِذَآ اَرَادَ شَيْـًٔاۖ اَنْ يَّقُوْلَ لَهٗ كُنْ فَيَكُوْنُ</p>
			<p class="translate">Innamā amruhū iżā arāda syai'an ay yaqūla lahū kun fa yakūn(u).</p>
			<p class="meaning">Sesungguhnya ketetapan-Nya, jika Dia menghendaki sesuatu, Dia hanya berkata kepadanya, “Jadilah!” Maka, jadilah (sesuatu) itu.</p>
		</li>
		<li>
			<p class="arabic" dir="rtl">فَسُبْحٰنَ الَّذِيْ بِيَدِهٖ مَلَكُوْتُ كُلِّ شَيْءٍ وَّاِلَيْهِ تُرْجَعُوْنَ ࣖ</p>
			<p class="translate">Fa subḥānal-lażī biyadihī malakūtu kulli syai'iw wa ilaihi turja‘ūn(a).</p>
			<p class="meaning">Maka, Mahasuci (Allah) yang di tangan-Nya kekuasaan atas segala sesuatu dan kepada-Nya kamu dikembalikan.</p>
		</li>
	</ol>
